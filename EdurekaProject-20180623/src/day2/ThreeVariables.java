package day2;

public class ThreeVariables {

	public static void main(String[] args) {
		
		//x, y, z to store data as 10, 83.57 and 'c'
		int x = 10;
		float y = 83.57f;
		char z = 'c';
		
		System.out.println("The int variable, x = " + x);
		System.out.println("The float variable, y = " + y);
		System.out.println("The char variable, z = "+ z);
		
	}

}
