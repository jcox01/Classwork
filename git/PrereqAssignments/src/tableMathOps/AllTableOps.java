package tableMathOps;

public class AllTableOps {

	public static void main(String[] args) {

	int a = 10;
	int b = 30;
	System.out.println("Addition \t" +a+ " + " +b+ " = " + (a + b));
	
	System.out.println("Subtraction \t" +a+ " - " +b+ " = " + (a - b));
	
	System.out.println("Division \t" +b+ " / " +a+ " = " + (b / a));

	System.out.println("Multiplication \t" +a+ " * " +b+ " = " + (a * b));

	for (int i = 0; i <= 5; i++) { 
		System.out.println("The value of i is :" +i);
	}		
	
	for (int i = 5; i >= 0; i--) {
		System.out.println("The value of i is: " +i);
	}
		
	}

}
